public class VetorEstatico {
	
    public static void main(String[] args) {
    	
    	int vetor[] = new int[10000];
    	int y = 200;
    	
    	while(y>=0){
    		for(int i=0; i<vetor.length; i++){
    			vetor[i]=0;
    		}
    		y--;
    		
    	}
    	
    } 
    	   
}